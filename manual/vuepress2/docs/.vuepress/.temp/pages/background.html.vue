<h2 id="reference-image" tabindex="-1"><a class="header-anchor" href="#reference-image" aria-hidden="true">#</a> Reference Image</h2>
<p>You can add an image of your choice on the background to be used for reference.
You can change the position and scale, for example if you want to move it on the screen corner.</p>
<p>Note that you can also set up the overlay level.
At overlay 0, the mesh will entirely occlude the reference image.
At overlay 1, the mesh will be occluded entirely by the image.
In-between values can be useful if you really want to use the reference image as a guide.</p>
