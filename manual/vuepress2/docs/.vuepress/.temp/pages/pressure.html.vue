<p>This menu lets you customize the behavior of the tablet pencil, such as the Apple Pencil or Samsung S Pen.</p>
<p>By default, the pressure of the pencil will impact the Radius and the Intensity of the current brush.
You can customize the falloff of this impact.</p>
<h2 id="allow-unrecognized-pressure" tabindex="-1"><a class="header-anchor" href="#allow-unrecognized-pressure" aria-hidden="true">#</a> Allow unrecognized pressure</h2>
<p>For some Android device, finger inputs will return an arbitrary fixed pressure value.
It can be annoying if you plan on sculpting with your finger, especially if the value returned is small.
In that case, make sure to disable this option.</p>
<p>By default, this option is enabled on iOS, but disabled for Android.</p>
