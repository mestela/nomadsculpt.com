<h2 id="gizmo" tabindex="-1"><a class="header-anchor" href="#gizmo" aria-hidden="true">#</a> Gizmo</h2>
<p>This tool lets you translate, rotate and scale your mesh with a single tool.</p>
<h4 id="method" tabindex="-1"><a class="header-anchor" href="#method" aria-hidden="true">#</a> Method</h4>
<table>
<thead>
<tr>
<th style="text-align:center">Method</th>
<th style="text-align:center">Description</th>
</tr>
</thead>
<tbody>
<tr>
<td style="text-align:center">Auto</td>
<td style="text-align:center">Let Nomad choose between Vertex or Object mode. Typically, object is preferred unless symmetry is enabled or if there is masking on the mesh</td>
</tr>
<tr>
<td style="text-align:center">Vertex</td>
<td style="text-align:center">Vertices are transformed individually. Symmetry and mask are taken into account. For primitives that are not validated, Object mode is forced</td>
</tr>
<tr>
<td style="text-align:center">Object</td>
<td style="text-align:center">The object is transformed as a whole. Symmetry and mask are ignored. If you use non-uniform scaling, Vertex mode will be forced</td>
</tr>
</tbody>
</table>
<h4 id="action" tabindex="-1"><a class="header-anchor" href="#action" aria-hidden="true">#</a> Action</h4>
<table>
<thead>
<tr>
<th style="text-align:center">Action</th>
<th style="text-align:center">Description</th>
</tr>
</thead>
<tbody>
<tr>
<td style="text-align:center">Move origin</td>
<td style="text-align:center">Move the mesh to the world origin, vertices are not modified</td>
</tr>
<tr>
<td style="text-align:center">Reset</td>
<td style="text-align:center">Reset the mesh transform to identity, vertices are not modified</td>
