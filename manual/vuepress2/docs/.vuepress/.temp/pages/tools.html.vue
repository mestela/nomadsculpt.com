<h2 id="transform" tabindex="-1"><a class="header-anchor" href="#transform" aria-hidden="true">#</a> Transform</h2>
<p>Translate, rotate and scale the model by using two fingers.
The interaction is more or less similar to the camera movement.</p>
<ul>
<li>Panning will translate the model</li>
<li>Pinching will scale the model</li>
<li>Twisting will rotate the model</li>
</ul>
<p>You can disable each of these movements separately, for example if you disable Scaling you will only be able to translate and rotate at the same time.</p>
